# CELL C3 — Convert PASCAL VOC XML → YOLO Format
# The Kaggle dataset uses XML annotations (PASCAL VOC).
# YOLO requires: class x_center y_center width height (normalized to [0,1])

import xml.etree.ElementTree as ET
from sklearn.model_selection import train_test_split

def convert_xml_to_yolo(xml_path, class_map):
    """Convert a single PASCAL VOC XML file to YOLO format lines."""
    try:
        tree  = ET.parse(xml_path)
        root  = tree.getroot()
        size  = root.find('size')
        img_w = int(size.find('width').text)
        img_h = int(size.find('height').text)

        if img_w == 0 or img_h == 0:
            return []

        lines = []
        for obj in root.findall('object'):
            cls_name = obj.find('name').text.strip().lower()
            if 'pothole' in cls_name or 'pot' in cls_name:
                cls_name = 'pothole'
            if cls_name not in class_map:
                continue

            cls_id = class_map[cls_name]
            bbox   = obj.find('bndbox')
            xmin   = max(0, min(float(bbox.find('xmin').text), img_w))
            ymin   = max(0, min(float(bbox.find('ymin').text), img_h))
            xmax   = max(0, min(float(bbox.find('xmax').text), img_w))
            ymax   = max(0, min(float(bbox.find('ymax').text), img_h))

            if xmax <= xmin or ymax <= ymin:
                continue

            x_center = (xmin + xmax) / 2 / img_w
            y_center = (ymin + ymax) / 2 / img_h
            width    = (xmax - xmin) / img_w
            height   = (ymax - ymin) / img_h

            lines.append(f'{cls_id} {x_center:.6f} {y_center:.6f} {width:.6f} {height:.6f}')

        return lines
    except Exception as e:
        print(f'  Warning: Failed to parse {xml_path}: {e}')
        return []


def build_yolo_dataset_c(raw_dir, output_dir):
    CLASS_MAP = {'pothole': 0}
    xml_files = list(Path(raw_dir).rglob('*.xml'))
    print(f'Found {len(xml_files)} XML annotation files')

    valid_pairs = []
    for xml_path in xml_files:
        img_path = None
        for ext in ['.jpg', '.jpeg', '.png', '.JPG', '.JPEG', '.PNG']:
            for candidate in [
                xml_path.parent / f'{xml_path.stem}{ext}',
                xml_path.parent.parent / 'images' / f'{xml_path.stem}{ext}'
            ]:
                if candidate.exists():
                    img_path = candidate
                    break
            if img_path:
                break

        if img_path is None:
            continue

        yolo_lines = convert_xml_to_yolo(str(xml_path), CLASS_MAP)
        if yolo_lines:
            valid_pairs.append((img_path, yolo_lines))

    print(f'Valid image-annotation pairs: {len(valid_pairs)}')
    if not valid_pairs:
        raise ValueError('No valid pairs found. Check dataset structure.')

    train_pairs, val_pairs = train_test_split(valid_pairs, test_size=0.2, random_state=42)

    for split_name, pairs in [('train', train_pairs), ('val', val_pairs)]:
        img_out = Path(output_dir) / split_name / 'images'
        lbl_out = Path(output_dir) / split_name / 'labels'
        img_out.mkdir(parents=True, exist_ok=True)
        lbl_out.mkdir(parents=True, exist_ok=True)
        for img_path, yolo_lines in pairs:
            shutil.copy2(img_path, img_out / img_path.name)
            with open(lbl_out / f'{img_path.stem}.txt', 'w') as f:
                f.write('\n'.join(yolo_lines))

    print(f'\n✓ Dataset C prepared:')
    print(f'   Train : {len(train_pairs)} images')
    print(f'   Val   : {len(val_pairs)} images')


