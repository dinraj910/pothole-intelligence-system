# CELL D2 — Cross-Model NMS
# Merges overlapping detections from all models into one clean output.
# Tracks which models agreed — consensus = high reliability signal.

import numpy as np

def compute_iou(box1, box2):
    """Intersection over Union. Format: [x1, y1, x2, y2] pixels."""
    x1 = max(box1[0], box2[0])
    y1 = max(box1[1], box2[1])
    x2 = min(box1[2], box2[2])
    y2 = min(box1[3], box2[3])
    intersection = max(0, x2 - x1) * max(0, y2 - y1)
    if intersection == 0:
        return 0.0
    area1 = (box1[2] - box1[0]) * (box1[3] - box1[1])
    area2 = (box2[2] - box2[0]) * (box2[3] - box2[1])
    union = area1 + area2 - intersection
    return intersection / union if union > 0 else 0.0


def cross_model_nms(all_detections, iou_threshold=0.45):
    """NMS across all model predictions. Tracks agreeing models per detection."""
    if not all_detections:
        return []

    sorted_dets = sorted(all_detections, key=lambda x: x['confidence'], reverse=True)
    kept, suppressed = [], set()

    for i, det_i in enumerate(sorted_dets):
        if i in suppressed:
            continue
        det_i['agreeing_models'] = [det_i['model_id']]
        for j, det_j in enumerate(sorted_dets):
            if j <= i or j in suppressed:
                continue
            if compute_iou(det_i['bbox'], det_j['bbox']) > iou_threshold:
                det_i['agreeing_models'].append(det_j['model_id'])
                suppressed.add(j)
        kept.append(det_i)

    return kept

print('✓ Cross-model NMS function ready.')

# CELL D3 — Full Inference Pipeline

import cv2

def run_multi_model_inference(image_path, conf_override=None):
    """
    Run all 3 models on a single image.
    Returns: annotated image (BGR), final detections list, raw count per model.
    """
    image = cv2.imread(image_path)
    if image is None:
        raise ValueError(f'Could not load image: {image_path}')

    h, w = image.shape[:2]
    all_raw_detections = []
    model_raw_counts   = {}

    # Step 1: Run all 3 models
    for model_id, info in MODELS.items():
        conf_threshold = conf_override if conf_override else info['conf']
        results  = info['model'](image, conf=conf_threshold, iou=0.45, verbose=False)
        raw_count = 0

        for result in results:
            if result.boxes is None:
                continue
            for i in range(len(result.boxes)):
                raw_count += 1
                xyxy     = result.boxes.xyxy[i].cpu().numpy()
                conf     = float(result.boxes.conf[i])
                cls_id   = int(result.boxes.cls[i])
                cls_name = result.names.get(cls_id, f'class_{cls_id}')
                all_raw_detections.append({
                    'model_id'  : model_id,
                    'model_name': info['name'],
                    'bbox'      : [float(xyxy[0]), float(xyxy[1]), float(xyxy[2]), float(xyxy[3])],
                    'confidence': conf,
                    'class_id'  : cls_id,
                    'class_name': cls_name,
                    'color'     : info['color'],
                })
        model_raw_counts[model_id] = raw_count

    # Step 2: Cross-model NMS
    final_detections = cross_model_nms(all_raw_detections, iou_threshold=0.45)

    # Step 3: Draw bounding boxes
    annotated  = image.copy()
    font       = cv2.FONT_HERSHEY_SIMPLEX
    font_scale = 0.55

    for det in final_detections:
        x1, y1, x2, y2 = [int(v) for v in det['bbox']]
        color  = det['color']
        label  = f"{det['model_id'][-1].upper()}: {det['class_name']} {det['confidence']:.2f}"

        # Thicker box if multiple models agreed
        thickness = 3 if len(det.get('agreeing_models', [])) > 1 else 2
        cv2.rectangle(annotated, (x1, y1), (x2, y2), color, thickness)

        # Label background + text
        text_size = cv2.getTextSize(label, font, font_scale, 1)[0]
        label_y   = max(y1 - 5, text_size[1] + 5)
        cv2.rectangle(annotated,
                      (x1, label_y - text_size[1] - 4),
                      (x1 + text_size[0] + 4, label_y + 2),
                      color, -1)
        cv2.putText(annotated, label, (x1 + 2, label_y - 2),
                    font, font_scale, (255, 255, 255), 1, cv2.LINE_AA)

    return annotated, final_detections, model_raw_counts

print('✓ Inference pipeline ready.')

